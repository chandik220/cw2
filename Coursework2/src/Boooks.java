public class Boooks {
    String BookName, AuthorName, date;
    int price, bookNumber, Quantity;

 

    Boooks(String BookName, String AuthorName, String date, int price, int bookNumber, int Quantity) {
        this.BookName = BookName;
        this.AuthorName = AuthorName;
        this.date = date;
        this.price = price;
        this.bookNumber = bookNumber;
        this.Quantity = Quantity;
    }

 

}